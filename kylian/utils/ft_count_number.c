#include "../cub3d.h"

int ft_count_number(char *src)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (src[j] && src[j] == '0')
		j++;
	while (src[i + j] && src[i + j] >= '0' && src[i + j] <= '9')
		i++;
	return (i);
}
